                        <div class="title"><h1>8 password security best practices</h1></div>

                        <p>We all know that password &ldquo;qwerty123&rdquo; and your birthday are really terrible passwords. We know that you shouldn&rsquo;t reuse in multiple sites and we know that you shouldn&rsquo;t write down your password yet many people do these things every day.</p>
                        <p>Today we're going to give you some password security best practices to incorporate into your personal safety routine.</p>
                        <p>We all agree that password protection is critical, but what can you do to secure your own accounts? You can protect against hacking attempts with some fairly thing and we list it for you.</p>
                        <p>&nbsp;</p>
                        <h3><strong>1. Never store them in plain text on your computer. </strong></h3>
                        <p>You can look for an online or local storage password manager that uses encryption but never store your password in plain text in your computer</p>
                        <p>&nbsp;</p>
                        <h3>2.&nbsp;<strong>Never write down you passwords</strong></h3>
                        <p>Even you are in your home or office room and you have trust to your family member or your team members it will give you trouble if someone got your password.</p>
                        <p>&nbsp;</p>
                        <h3><strong>3. Never use your personal information</strong></h3>
                        <p>Never use your real name, username, birthday, phone number or any other easily identifiable information in your password.</p>
                        <p>&nbsp;</p>
                        <h3><strong>4. Never use an easy keyboard combination like ABCD or QWERTY </strong></h3>
                        <p>&lt;blank&gt;</p>
                        <p>&nbsp;</p>
                        <h3>5.&nbsp;<strong>Be careful with the answers to security questions. </strong></h3>
                        <p>​​​​​​​If you feel like any of them are strong enough you use an unrelated answer that you know you can remember</p>
                        <p>&nbsp;</p>
                        <h3><strong>6. Never share your password.</strong></h3>
                        <p>But if you have to send your password to someone for some important reason. Use an encryption tool like Protected Message. you can click here to see how it works.</p>
                        <p>&nbsp;</p>
                        <h3><strong>7. Never reuse a password especially for your email.</strong></h3>
                        <p>​​​​​​​Many people are guilty of reusing passwords including myself in the past but please at the least use a uique and strong password.</p>
                        <p>&nbsp;</p>
                        <h3><strong>8. Use 2-Factor authentication whenever possible.</strong></h3>
                        <p>​​​​​​​The two factor authentication, you can have security codes sent to your mobile device or email in order to double check that its actually you trying to log in. this can really help against things like key loggers skimming your passwords as this code are one-time use.</p>



